function deleteFile(ref, key) {
  const storageRef = fireStorage.ref(ref);
  const fileRef = storageRef.child(key);
  fileRef
    .delete()
    .then((res) => {
      console.log(res);
    })
    .catch((err) => {
      console.log(err);
    });
}
