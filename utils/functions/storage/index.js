function uploadFile(file, reference) {
  const storageRef = fireStorage.ref(reference);
  const fileRef = storageRef.child(file.name);
  fileRef
    .put(file)
    .then((result) => {
      console.log(result);
    })
    .catch((err) => {
      console.error(err);
    });
}

