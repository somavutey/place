function getUrl(ref, key) {
  const storageRef = fireStorage.ref(ref);
  const fileRef = storageRef.child(key);
  fileRef
    .getDownloadURL()
    .then((res) => {
      console.log("URL::", res);
    })
    .catch((err) => {
      console.log(err.message);
    });
}

